<p>Tips for using the form with students: Prior to the sequence of observing the student four times, review the PCOF form with them. Also, let students know that they can choose subsets of these skills to focus on during three of the four observations. If the learner is trying a new skill, It may be helpful for the students to pick only 1-2 specific skills to practice during the observation. Focusing on specific skills does not preclude the student using others skills during any encounter or the observer rating skills other than those mentioned by the student. However, encouraging the student to focus on subsets of skills decreases the chance that the student (or you) will feel overwhelmed.</p>


<p><strong>Important principles of effective feedback:</strong></p>
<ol><li>Ask the student to reflect on how the encounter went, focusing on pre-determined skills to focus on</li>
